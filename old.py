class Node:

    def __init__(self,position = None,parent = None):

        self.position = position
        self.parent = parent

        self.f = 0 #g + h
        self.g = 0 #distance from node to start
        self.h = 0 #distance from node to end

def astar(maze,start,end):
    start_node = Node(start,None)
    start_node.f = start_node.g = start_node.h = 0
    end_node = Node(end,None)
    start_node.f = start_node.g = start_node.h = 0

    open_list = [] #nodes to be checked
    closed_list = [] #nodes already checked

    open_list.append(start_node)

    adjacent_squares = ((0, -1), (0, 1), (-1, 0), (1, 0),)

    while len(open_list) > 0:
        current_node = open_list[0]
        current_node_index = 0

        #this loop finds the node with the lowest f cost. if 2 values have the same f cost, then it finds the one with the lower h cost
        for i in range(len(open_list)):
            if open_list[i].f < current_node.f:
                current_node = open_list[i]
                current_node_index = i
            if open_list[i].f == current_node.f:
                if open_list[i].h < current_node.h:
                    current_node = open_list[i]
                    current_node_index = i
        
        open_list.pop(current_node_index)
        closed_list.append(current_node)

        #return path if solved
        if current_node.position == end_node.position:
            path = []
            current = current_node
            while current != None:
                path.append(current.position)
                current = current.parent
            return path[::-1]  # Return reversed path

        children = []
        for new_position in adjacent_squares: # Adjacent squares

            # Get node position
            node_position = (current_node.position[0] + new_position[0], current_node.position[1] + new_position[1])

            #make sure node in range
            if node_position[0] > len(maze) - 1 or node_position[0] < 0 or node_position[1] > len(maze[0]) - 1 or node_position[1] < 0:
                continue
            
            #make sure walkable terrain
            if maze[node_position[0]][node_position[1]] == 1:
                continue

            #create new node
            new_node = Node(node_position, current_node)

            #append to children
            children.append(new_node)
        
        for child in children:

            # Child is on the closed list
            if len([closed_child for closed_child in closed_list if closed_child == child]) > 0:
                continue

            # Create the f, g, and h values
            child.g = current_node.g + 1
            child.h = ((child.position[0] - end_node.position[0]) ** 2) + ((child.position[1] - end_node.position[1]) ** 2)
            child.f = child.g + child.h

            # Child is already in the open list
            if len([open_node for open_node in open_list if child.position == open_node.position and child.g > open_node.g]) > 0:
                continue

            # Add the child to the open list
            open_list.append(child)

    return None
    
